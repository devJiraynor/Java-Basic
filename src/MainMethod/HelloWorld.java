//# Java는 폴더를 Package로 다룸 
package MainMethod;

// 주석: 코드에 설명을 작성하거나 실행 시키고 싶지 않은 코드를 주석처리할 수 있음
// 한줄 주석 : 한줄 전체가 주석됨
/* 공간 주석 : 주석 종료 키워드를 만날 때 까지의 모든 코드르 주석처리 함 */
//# Java는 .java 파일에 class 혹은 interface가 있어야 함 
public class HelloWorld {
    //# Java는 main method로 실행 프로그램을 실행 
    public static void main(String[] args) {
        //# 자바 구문의 끝에는 ;를 붙여야 해당 코드가 종료됨을 인식함 
        System.out.println("Hello World!");
    }
}
